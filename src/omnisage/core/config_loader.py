"""
Simplified configuration loading utilities.
Handles novel YAML formats with template_type field support.
Supports num_chapter and max_words auto-configuration.
"""

import yaml
from pathlib import Path
from typing import Dict, Any

from omnisage.utils.logger import get_logger

logger = get_logger(__name__)

class ConfigLoader:
    """Loads and validates configuration files."""
    
    @staticmethod
    def load_scheme(scheme_path: str) -> Dict[str, Any]:
        """Load and validate scheme configuration."""
        path = Path(scheme_path)
        
        if not path.exists():
            raise FileNotFoundError(f"Scheme file not found: {scheme_path}")
        
        try:
            with open(path, 'r', encoding='utf-8') as f:
                scheme = yaml.safe_load(f)
            
            # Validate required fields for outline planning
            required_fields = ["title", "language"]
            for field in required_fields:
                if field not in scheme:
                    raise ValueError(f"Missing required field in scheme: {field}")
            
            # 处理章节数配置
            num_chapters = ConfigLoader._get_configured_chapters(scheme)
            max_words = scheme.get('max_words', 30000)
            
            # Log configuration info
            logger.info(f"Scheme configuration:")
            logger.info(f"  - Title: {scheme.get('title')}")
            logger.info(f"  - Chapters: {num_chapters}")
            logger.info(f"  - Max words: {max_words}")
            logger.info(f"  - Words per chapter: ~{max_words // num_chapters if num_chapters > 0 else 0}")
            
            # For outline planning, chapters field is optional (will be generated)
            # If chapters exist, validate them
            if "chapters" in scheme:
                actual_chapters = []
                for chapter in scheme.get("chapters", []):
                    chapter_id = str(chapter.get("chapter", ""))
                    
                    # Validate chapter structure
                    ConfigLoader._validate_chapter_structure(chapter, chapter_id)
                    actual_chapters.append(chapter_id)
                
                # Set actual chapter count
                scheme["num_chapters"] = len(actual_chapters)
                logger.info(f"Loaded scheme with existing {len(actual_chapters)} chapters")
                
                # 验证现有章节数与配置是否一致
                configured_chapters = ConfigLoader._get_configured_chapters(scheme)
                if len(actual_chapters) != configured_chapters:
                    logger.warning(f"Existing chapters ({len(actual_chapters)}) != configured chapters ({configured_chapters})")
            else:
                # No chapters field - will be generated by OutlinePlanner
                scheme["num_chapters"] = 0
                logger.info("Loaded scheme for outline generation (no existing chapters)")
            
            # Log template information
            template_type = scheme.get("template_type", "")
            genre = scheme.get("genre", "")
            
            if template_type:
                logger.info(f"Template type: {template_type}")
            elif genre:
                logger.info(f"Genre: {genre}")
            
            return scheme
            
        except yaml.YAMLError as e:
            raise ValueError(f"Invalid YAML in scheme file: {e}")
    
    @staticmethod
    def _get_configured_chapters(scheme: Dict[str, Any]) -> int:
        """获取配置的章节数，支持多种字段名"""
        # 优先级: num_chapter > num_chapters > 默认值
        if 'num_chapter' in scheme:
            num_chapters = scheme['num_chapter']
            logger.info(f"Found num_chapter configuration: {num_chapters}")
            return num_chapters
        else:
            default_chapters = 5
            logger.info(f"No chapter count configuration found, using default: {default_chapters}")
            return default_chapters
    
    @staticmethod
    def _validate_chapter_structure(chapter: Dict[str, Any], chapter_id: str):
        """Validate individual chapter structure."""
        # Validate required fields
        required_fields = ["chapter", "title"]
        for field in required_fields:
            if field not in chapter:
                raise ValueError(f"Chapter {chapter_id} missing required field: {field}")
        
        # Validate plot_summary exists (required for dynamic analysis)
        if "plot_summary" not in chapter:
            logger.warning(f"Chapter {chapter_id} missing plot_summary - may affect analysis quality")
        
        # Validate chapter structure for enhanced workflow
        recommended_fields = ["chapter_type", "key_elements", "expected_changes"]
        missing_recommended = []
        
        for field in recommended_fields:
            if field not in chapter:
                missing_recommended.append(field)
        
        if missing_recommended:
            logger.info(f"Chapter {chapter_id} missing recommended fields for enhanced analysis: {missing_recommended}")
    
    @staticmethod
    def load_llm_mapping(mapping_path: str) -> Dict[str, Any]:
        """Load and validate LLM mapping configuration."""
        path = Path(mapping_path)
        
        if not path.exists():
            raise FileNotFoundError(f"LLM mapping file not found: {mapping_path}")
        
        try:
            with open(path, 'r', encoding='utf-8') as f:
                mapping = yaml.safe_load(f)
            
            # Validate required fields
            if "models" not in mapping:
                raise ValueError("Missing 'models' section in LLM mapping")
            if "function_model_mapping" not in mapping:
                raise ValueError("Missing 'function_model_mapping' section in LLM mapping")
            
            # Validate that required roles have corresponding models
            required_roles = ["writer"]  # Only writer is required for basic pipeline
            
            for role in required_roles:
                if role not in mapping["function_model_mapping"]:
                    raise ValueError(f"Missing required role '{role}' in function_model_mapping")
                
                model_key = mapping["function_model_mapping"][role]
                if model_key not in mapping["models"]:
                    raise ValueError(f"Model '{model_key}' referenced by role '{role}' not found in models")
            
            logger.info(f"Loaded LLM mapping with {len(mapping['models'])} models")
            return mapping
            
        except yaml.YAMLError as e:
            raise ValueError(f"Invalid YAML in LLM mapping file: {e}")
    
    @staticmethod
    def load_worker_config(worker_config_path: str = "config/worker_config.yaml") -> Dict[str, Any]:
        """Load worker configuration."""
        path = Path(worker_config_path)
        
        if not path.exists():
            raise FileNotFoundError(f"Worker config file not found: {worker_config_path}")
        
        try:
            with open(path, 'r', encoding='utf-8') as f:
                worker_config = yaml.safe_load(f)
            
            logger.info(f"Loaded worker configuration from {worker_config_path}")
            return worker_config
            
        except yaml.YAMLError as e:
            raise ValueError(f"Invalid YAML in worker config file: {e}")
    
    @staticmethod
    def load_compilation_config(compilation_config_path: str = "config/compilation_config.yaml") -> Dict[str, Any]:
        """Load compilation template configuration."""
        path = Path(compilation_config_path)
        
        if not path.exists():
            raise FileNotFoundError(f"Compilation config file not found: {compilation_config_path}")
        
        try:
            with open(path, 'r', encoding='utf-8') as f:
                compilation_config = yaml.safe_load(f)
            
            logger.info(f"Loaded compilation configuration from {compilation_config_path}")
            return compilation_config
            
        except yaml.YAMLError as e:
            raise ValueError(f"Invalid YAML in compilation config file: {e}")
    
    @staticmethod
    def validate_template_availability(scheme: Dict[str, Any]) -> bool:
        """Validate that the required template file exists."""
        template_type = scheme.get("template_type", "")
        genre = scheme.get("genre", "")
        
        # Determine which template file to look for
        template_name = template_type if template_type else genre
        
        if not template_name:
            logger.warning("No template_type or genre specified in scheme")
            return False
        
        template_path = Path("scheme") / "template" / f"{template_name}.yaml"
        
        if template_path.exists():
            logger.info(f"Template file found: {template_path}")
            return True
        else:
            logger.warning(f"Template file not found: {template_path}")
            return False